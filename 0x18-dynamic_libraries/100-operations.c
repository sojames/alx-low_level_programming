/**
* add - addition
* @x: number
* @y: nuber
* Return: result
*/
int add(int x, int y)
{
return (x + y);
}
/**
* sub - subtraction
* @x: number
* @y: nuber
* Return: result
*/
int sub(int x, int y)
{
return (x - y);
}
/**
* mul - multiplication
* @x: number
* @y: nuber
* Return: result
*/
int mul(int x, int y)
{
return (x * y);
}
/**
* div - division
* @x: number
* @y: nuber
* Return: result
*/
int div(int x, int y)
{
return (x / y);
}
/**
* mod - modulus
* @x: number
* @y: number
* Return: result
*/
int mod(int x, int y)
{
return (x % y);
}

